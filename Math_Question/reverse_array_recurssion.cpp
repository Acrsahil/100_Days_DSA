void reve(vector<int> &v,int s, int e){
  if(s>=e){
    return;
  }
  swap(v[s],v[e]);
  reve(v,s+1,e-1);
}
void reve2(vector<int> &v,int i,int n){
  if(i>=n/2){
    return;
  }
  swap(v[i],v[n-i-1]);
  reve2(v,n,i+1);
}

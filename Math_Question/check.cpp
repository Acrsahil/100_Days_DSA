bool sieve[1000001];
int N = 1000000;

void create_seive(){
  sieve[1] = true; 
  sieve[0] = true; 
  for (int i = 2; i*i<=N; i++) {
    if(sieve[i] == false){ // not composite
      for(int j = i*i; j<=N; j+=i){
        sieve[j] = true; // composite
      }
    }
  }
}

void solve(){
  create_seive();
  int t; cin >> t;
  while(t--){
    int n; cin >> n;

    if(sieve[n] == false){
      cout << "Prime" << endl;
    }else {
      cout << "Not Prime" << endl;
    }
  }
}

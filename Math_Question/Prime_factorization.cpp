const int N = 100000000;
int spf[N+1];

void create_Seive(){
  for(int i = 1; i<=N; i++){
    spf[i] = i;
  }

  for(int i = 2; i*i<=N; i++){
    if(spf[i] == i){
      for(int j = i*i; j<=N; j+=i){
        if(spf[j] == j){
          spf[j] = i;
        }
      }
    }
  }
}

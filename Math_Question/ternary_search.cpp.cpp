int solve(vector<int>v,int k){
  int l = 0;
  int r = v.size()-1;

  while(l<=r){
    int m1 = l+(r-l)/3;
    int m2 = r-(r-l)/3;

    if(v[m1] == k){
      return m1;
    }else if(v[m2] == k){
      return m2;
    }else if(k<v[m1]){
      r = m1-1;
    }else if(k>v[m2]){
      l = m2+1;
    }else{
      l = m1+1;
      r = m2-1;
    }
  }
  return -1;
}

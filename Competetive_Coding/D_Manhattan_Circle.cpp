void solve(){
  int n,m; cin >> n >> m;
  char arr[n][m];
  for(int i = 0; i<n; i++){
    for(int j = 0; j<m; j++){
      cin >> arr[i][j];
    }
  }

int s = -1;
int e = -1;

for(int i = 0; i<n; i++){
  for(int j = 0; j<m; j++){
    if(arr[i][j] == '#'){
      s = i;
        break;
    }
  }
}

for(int i = n-1; i>=0; i--){
  for(int j = m-1; j>=0; j--){
    if(arr[i][j] == '#'){
      e = i;
        break;
    }
  }
}

  int mid = (s+e)/2;
  // inside mid find
  int st = -1;
  int ed = -1;

  for(int i = 0; i<m; i++){
    if(arr[mid][i] == '#'){
      st = i;
      break;
    }
  }
  for(int i = m-1; i>=0; i--){
    if(arr[mid][i] == '#'){
      ed = i;
      break;
    }
  }

  int ans2 = (st+ed)/2;
  cout << ++mid << " " << ++ans2 << endl;

}

void solve(){
  int n,m; cin >> n >> m;
  int arr[n+2][m+2] ;
  for(int i = 0; i<n+2; i++){
    for(int j = 0; j<m+2; j++){
      arr[i][j] = 0;
    }
  }

  for(int i = 1; i<n+1; i++){
    for(int j = 1; j<m+1; j++){
      cin >> arr[i][j];
    }
  }

  int maxi = INT_MIN;
  for(int i = 1; i<n+1; i++){
    for(int j = 1; j<m+1; j++ ){
      if(arr[i][j]>arr[i-1][j] && arr[i][j]>arr[i+1][j] && arr[i][j]>arr[i][j-1] && arr[i][j]>arr[i][j+1]){
        int maxi = max(max(arr[i-1][j],arr[i+1][j]),max(arr[i][j-1],arr[i][j+1]));
        arr[i][j] = maxi;
      }
    }
  }
  for(int i = 1; i<n+1; i++){
    for(int j = 1; j<m+1; j++){
      cout << arr[i][j] << " ";
    }
    cout << endl;
  }
}

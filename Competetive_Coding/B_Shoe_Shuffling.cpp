void solve(){
  int n;cin >> n;
  vector<int>v(n+1);
  vector<int>ans;
  map<ll,ll>mp;

  for(int i = 1; i<=n; i++) {
    cin >> v[i];
    mp[v[i]]++;
  }
  for(auto i : mp){
    if(i.second == 1){
      cout << "-1" << endl;
      return;
    }
  }

  for(int i = 1; i<=n; i++){
    vector<int>pos;
    pos.push_back(i);
    ll j = i+1;
    while( j <= n && v[j] == v[i]){
      pos.push_back(j);
      j++;
    }
    i = j-1;
    ans.push_back(pos[pos.size()-1]);

    for(ll k = 0; k<pos.size()-1; k++){
      ans.push_back(pos[k]);
    }

  }
    for(auto i : ans) cout << i << " ";
    cout << endl;

}

void solve(){
  int n;
  cin >>n;
  vector<int>v(n);
  int start = -1,end = -1;
  vector<int> tests(n);

  for(int i = 0; i<n; i++){
    cin >> v[i];
    tests[i] = v[i];
  }
  sort(v.begin(),v.end());
  for(int i = 0; i<n; i++){
    if(v[i] != tests[i]){
      if(start == -1){
        start = i;
      }
      end = i + 1;
    }
  }
  reverse(tests.begin()+start,tests.begin()+end);

  if(is_sorted(tests.begin(),tests.end())){
    if(start == -1){
      start = 0;
    }
    if(end == -1){
      end = 1;
    }
    cout << "yes" << endl;
    cout << ++start << " " << end << endl;
  }else{
    cout << "no" << endl;
  }
}

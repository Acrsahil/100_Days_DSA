void solve(){
  int n,k;
  cin >> n >> k;
  vector<int> v(n);
  int ans = INT_MAX;

  for(int i = 0; i<n; i++){
    cin >> v[i];
  }
  int j = 0;
  int i = 1;
  int sum = 0;
  int ansi = 0;
  while(j<k){
    sum += v[j];
    j++;
  }
  ans = min(ans,sum);
  while(i<=n-k && j<n){
    sum = sum - v[i-1];
    sum = sum + v[j];

    if(sum < ans){
      ansi = i;
      ans = sum;
    }
    i++;
    j=j+1;
  }
  cout << ansi+1 << endl;

}

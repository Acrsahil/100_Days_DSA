void solve(){
  int a,b,c;
  cin >> a >> b >> c;
  int len = a*b*c;
  map<int, int>mp;
  for(int i = 1; i<=len; i++){
    for(int j = i; j<=len; j+=i){
        mp[j]++;
      }
  }
  // debug(mp);
  long ans = 0;
  long mod = 1073741824;
  for(int i = 1; i<=a; i++){
    for(int j = 1; j<=b; j++){
      for(int k = 1; k<=c; k++){
        ll div = mp[i*j*k];
        debug(div);
        ans = (ans+ div)%mod;
      }
    }
  }
  // debug(count);
  cout << ans << endl;
}

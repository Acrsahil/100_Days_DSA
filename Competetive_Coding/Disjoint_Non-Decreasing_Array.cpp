void solve(){
  ll n; cin >> n;
  vector<ll>v(n);
  for(ll i = 0; i<n; i++ ) {
    cin >> v[i];
  }
  ll breakp = -1;
  for(int i = 0; i<n-1; i++){
    if(v[i]>v[i+1]){
      breakp = i;
      break;
    }
  }
  if(breakp == -1 || breakp +2 >= n){
    cout << "YES" << endl;
    return;
  }

    ll lower = v[breakp]-v[breakp+1];
    ll upper = -1;

  if(breakp+2 <n){
    upper = v[breakp+2] - v[breakp+1];
  }
  debug(lower);
  debug(upper);

  ll i = breakp;
  while(i<n-1){
    if(v[i]>v[i+1]){
      lower = max(lower,v[i]-v[i+1]);

      if(i+2<n) upper = min(upper,v[i+2]-v[i+1]);
      i+=2;
    }
    else i++;
  }

  if(lower<=upper) cout << "YES" << endl;
  else cout << "NO" << endl;

}

void solve(){

  int n,k; cin >> n >> k;
  vector<int>a(n);
  vector<int>b(n);

  for(int i = 0; i<n; i++ ){
    cin >> a[i];
    b[i] = a[i];
  }
  k--;
  swap(a[0],a[k]);


  int ans1 = 0,ans2 = 0;
  for(int i = 1; i<n; i++){
    if(a[i] < a[0]){
      ans1++;
    }else{
      break;
    }
  }
  // search for the maximum element just from b[k];
  for(int i = 0; i<k; i++){
    if(b[i]>b[k]){
      swap(b[i],b[k]);
        k = i;
      break;
    }
  }

  if( k != 0){
    ans2++;
  }
  for(int i = k+1; i<n; i++){
    if(b[i] < b[k]){
      ans2++;
    }else{
      break;
    }
  }
  cout << max(ans1,ans2) << endl;
}

void solve(){
  int n,m;
  cin >> n >> m;
  vector<int> right(n);
  vector<int> wrong(m);
  int min_element = INT_MAX;
  int max_element = INT_MIN;

  for(int i = 0; i<n; i++){
    cin >> right[i];
    min_element = min(min_element,right[i]);
    max_element = max(max_element,right[i]);
  }
  int limit = INT_MAX;
  for(int i = 0; i<m; i++){
    cin >> wrong[i];
    limit = min(limit,wrong[i]);
  }
  int ans = max(2*min_element,max_element);
  if(ans<limit){
    cout << ans << endl;
  }else{
    cout << -1 << endl;
  }

}
